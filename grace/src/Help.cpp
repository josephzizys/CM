void SymbolHelp::setHelpRoot(String help, String root) {
  maproots.set(help, root);
}

String SymbolHelp::getHelpRoot(String help) {
  return maproots[help] ;
}

String SymbolHelp::lookupHelp(String symbol, String helppath) {
  // search for symbol help in colon delimited help path.
  int i, p=0, l=helppath.length();
  String s, h, r, x;
  while (helppath != String::empty) {
    x=String::empty;
    i=helppath.indexOfChar(':');
    if (i==-1) {
      h=helppath;
      helppath=String::empty;
    }
    else {
      h=helppath.substring(0,i);
      helppath=helppath.substring(i+1);
    }
    //printf("helppath='%s'  help='%s'\n", helppath.toUTF8(), h.toUTF8());
    if ( h == T("Sal") )
      x=salmap[symbol];
    else if ( h == T("GraceScheme") )
      x=graceschememap[symbol];
    else if ( h == T("Scheme") )
      x=schememap[symbol];
    else if ( h == T("CommonMusic") )
      x=cmmap[symbol];
    else if ( h == T("CommonLispMusic") )
      x=clmmap[symbol];
    else if ( h == T("Fomus") ) 
      x=fomusmap[symbol];
    else if ( h == T("CommonLisp") ) 
      x=clmap[symbol];
    else
      x=String::empty;
    if ( x != String::empty) {
      return getHelpRoot( h ) + x;
    }
  }
  return String::empty;
}

void SymbolHelp::openHelpInBrowser(String help) {
  URL url=URL(help);
  url.launchInDefaultBrowser();
}

